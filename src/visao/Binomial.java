    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        //lÃª valores dos jtextfields
        String tamanho = jTextField1.getText();
        String probabilidade = jTextField2.getText();
        String pontoX = jTextField3.getText();
        
        //converter para double
        int n = Integer.parseInt(tamanho);
        double p = Double.parseDouble(probabilidade);
        int x = Integer.parseInt(pontoX);
        
        //instancia uma nova classe
        Calculo calculo = new Calculo();
        
        //variaveis para calculo binomial
        double fatN, fatX, fatNX, c, q, resultado = 0; 
        int a, i=0;
        
        if (jComboBox2.getSelectedItem().toString().equals("=")){
            fatN = calculo.fatorial(n);
            fatX = calculo.fatorial(x);
            a = n - x;
            fatNX = calculo.fatorial(a);
        
        
            c = calculo.combinacao(fatN, fatX, fatNX);
            q = (1-p);
            resultado = c*(pow(p, x))*(pow(q, n-x));
            JOptionPane.showMessageDialog( null, "Resultado: \n" + resultado);
        }else if (jComboBox2.getSelectedItem().toString().equals("<")){
            fatN = calculo.fatorial(n);
            for (i=0;i<x;i++){
                fatX = calculo.fatorial(i);
                a = n - i;
                fatNX = calculo.fatorial(a);
                
                c = calculo.combinacao(fatN, fatX, fatNX);
                q = (1-p);
                resultado += c*(pow(p, i))*(pow(q, n-i));
            }
            JOptionPane.showMessageDialog(null, resultado);
        } else if (jComboBox2.getSelectedItem().toString().equals(">")){
            fatN = calculo.fatorial(n);
            for (i=x+1;i<=n;i++){
                fatX = calculo.fatorial(i);
                a = n - i;
                fatNX = calculo.fatorial(a);
                
                c = calculo.combinacao(fatN, fatX, fatNX);
                q = (1-p);
                resultado += c*(pow(p, i))*(pow(q, n-i));
            }
            JOptionPane.showMessageDialog(null, resultado);
        } else if (jComboBox2.getSelectedItem().toString().equals("<=")){
            fatN = calculo.fatorial(n);
            for (i=0;i<=x;i++){
                fatX = calculo.fatorial(i);
                a = n - i;
                fatNX = calculo.fatorial(a);
                
                c = calculo.combinacao(fatN, fatX, fatNX);
                q = (1-p);
                resultado += c*(pow(p, i))*(pow(q, n-i));
            }
            JOptionPane.showMessageDialog(null, resultado);
        } else if (jComboBox2.getSelectedItem().toString().equals(">=")){
            fatN = calculo.fatorial(n);
            for (i=x;i<=n;i++){
                fatX = calculo.fatorial(i);
                a = n - i;
                fatNX = calculo.fatorial(a);
                
                c = calculo.combinacao(fatN, fatX, fatNX);
                q = (1-p);
                resultado += c*(pow(p, i))*(pow(q, n-i));
            }
            JOptionPane.showMessageDialog(null, resultado);
        }
            
    }//GEN-LAST:event_jButton1ActionPerformed

