    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double fatX;
        double e = 2.718281828;
        double resultado = 0;
        String posicao = jTextField2.getText();
        String Lamb = jTextField1.getText();
        double L = Double.parseDouble(Lamb);
        int x = Integer.parseInt(posicao);
        int i;
        Calculo calculo = new Calculo();
        
        if (jComboBox2.getSelectedItem().toString().equals("=")){
            fatX = calculo.fatorial(x);
            resultado = (pow(e, -L) * pow (L, x))/fatX;
            JOptionPane.showMessageDialog(null, resultado);
        }else if (jComboBox2.getSelectedItem().toString().equals("<")){
            for (i=0;i<x;i++){
                fatX = calculo.fatorial(i);
                resultado += (pow(e, -L) * pow (L, i))/fatX;
            }
            JOptionPane.showMessageDialog(null, resultado);
        } else if (jComboBox2.getSelectedItem().toString().equals(">")){
            for (i=0;i<=x;i++){
                fatX = calculo.fatorial(i);
                resultado += (pow(e, -L) * pow (L, i))/fatX;
            }
            resultado = 1 - resultado;
            JOptionPane.showMessageDialog(null, resultado);

        } else if (jComboBox2.getSelectedItem().toString().equals("<=")){
            for (i=0;i<=x;i++){
                fatX = calculo.fatorial(i);
                resultado += (pow(e, -L) * pow (L, i))/fatX;
            }
            JOptionPane.showMessageDialog(null, resultado);
        } else if (jComboBox2.getSelectedItem().toString().equals(">=")){
            for (i=0;i<x;i++){
                fatX = calculo.fatorial(i);
                resultado += (pow(e, -L) * pow (L, i))/fatX;
            }
            resultado = 1 - resultado;
            JOptionPane.showMessageDialog(null, resultado);
        }
        
       

    }//GEN-LAST:event_jButton1ActionPerformed

